"""hybrid-python backend: compile a pattern-spec into a Python harness.

Scores high when ``target_runtime`` is python. Emits a deterministic *scaffold*
-- the uniform CLI, IO + schema validation, one typed function stub per spec
step, and golden/perturbed test scaffolds. Claude fills the deterministic step
bodies and the structural assertions from the spec (the scripts do plumbing; the
model writes logic). ``semantic`` steps become a schema-validated ``semantic.call``
and the harness pulls in ``semantic.py``; a spec with no semantic step degrades to
pure code (no SDK dependency).
"""

from __future__ import annotations

import json
import subprocess
import sys
import time
from pathlib import Path

from ..base import VerifyResult

_HERE = Path(__file__).resolve().parent

MANIFEST_NAME = ".spec-learner.json"

RUN_PY = '''#!/usr/bin/env python3
"""Uniform CLI for this harness. Generated by spec-learner (hybrid-python).

    run.py --input <path|-> --out <path|->

Reads the variable inputs as JSON (``-`` = stdin), runs the ordered pipeline,
validates the result against ``output.schema.json``, and exits non-zero on any
schema violation. It never writes malformed output.
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

import pipeline

_HERE = Path(__file__).resolve().parent
_SCHEMA = json.loads((_HERE / "output.schema.json").read_text())


def _read(src: str) -> dict:
    text = sys.stdin.read() if src == "-" else Path(src).read_text()
    return json.loads(text)


def _write(dst: str, data: dict) -> None:
    text = json.dumps(data, indent=2, sort_keys=True)
    if dst == "-":
        sys.stdout.write(text + "\\n")
    else:
        Path(dst).write_text(text + "\\n")


def main(argv: list[str] | None = None) -> int:
    import jsonschema

    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--input", required=True, help="input JSON path or - for stdin")
    ap.add_argument("--out", required=True, help="output JSON path or - for stdout")
    args = ap.parse_args(argv)

    result = pipeline.run(_read(args.input))

    errors = sorted(
        jsonschema.Draft7Validator(_SCHEMA).iter_errors(result),
        key=lambda e: list(e.path),
    )
    if errors:
        for err in errors:
            loc = "/".join(str(p) for p in err.path) or "<root>"
            print(f"output schema violation at {loc}: {err.message}", file=sys.stderr)
        return 1

    _write(args.out, result)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
'''

CONFTEST_PY = '''"""Put the harness root on sys.path so tests can ``import pipeline``."""
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
'''


def _fn_name(step_id: str) -> str:
    """Turn a step id (``apply-availability``) into a function name."""
    return step_id.strip().replace("-", "_").replace(" ", "_")


def _has_semantic(spec: dict) -> bool:
    return bool(spec.get("semantic_steps"))


def _variable_fields(spec: dict) -> list[dict]:
    return [f for f in spec["input_contract"]["fields"] if f["variability"] == "variable"]


def _fixed_fields(spec: dict) -> list[dict]:
    return [f for f in spec["input_contract"]["fields"] if f["variability"] == "fixed"]


def _render_pipeline(spec: dict) -> str:
    """Generate ``pipeline.py``: one stub per step, threaded by ``run``.

    Convention: every step takes the shared ``state`` dict (seeded with
    ``state["input"]``), reads what it needs, and writes its products back. The
    final step must set ``state["output"]`` -- that is what ``run`` returns and
    what ``run.py`` validates against ``output.schema.json``.
    """
    variable = ", ".join(f["name"] for f in _variable_fields(spec)) or "(none)"
    fixed = ", ".join(f["name"] for f in _fixed_fields(spec)) or "(none)"

    lines = [
        '"""Pipeline for this harness. Generated scaffold (hybrid-python).',
        "",
        "One function per spec step, run in order by ``run``. Fill each",
        "deterministic body from the spec; do NOT hardcode any value that the",
        "input_contract marks 'variable' -- read those from ``state['input']``.",
        "",
        f"Variable inputs (harness dimensions): {variable}",
        f"Fixed inputs (constants): {fixed}",
        '"""',
        "from __future__ import annotations",
    ]
    if _has_semantic(spec):
        lines += [
            "from pathlib import Path",
            "",
            '_PROMPTS = Path(__file__).resolve().parent / "prompts"',
        ]
    lines += ["", ""]

    fn_names: list[str] = []
    for step in spec["steps"]:
        fn = _fn_name(step["id"])
        fn_names.append(fn)
        lines.append(f"def {fn}(state: dict) -> None:")
        lines.append(f'    """[{step["kind"]}] {step["description"]}"""')
        if step["kind"] == "semantic":
            lines.append("    from semantic import call")
            lines.append("")
            lines.append("    # TODO(spec-learner): build the payload + out_schema for this step.")
            lines.append(f"    state[{step['id']!r}] = call(")
            lines.append(f'        prompt=(_PROMPTS / "{step["id"]}.txt").read_text(),')
            lines.append('        payload=state["input"],')
            lines.append("        out_schema={},  # TODO: the step's output schema")
            lines.append("    )")
        else:
            msg = f"TODO(spec-learner): implement step {step['id']!r}"
            lines.append(f"    raise NotImplementedError({msg!r})")
        lines.append("")
        lines.append("")

    chain = ", ".join(fn_names)
    lines.append("def run(data: dict) -> dict:")
    lines.append('    """Run the ordered steps and return the validated output."""')
    lines.append('    state: dict = {"input": data}')
    lines.append(f"    for step in ({chain}{',' if len(fn_names) == 1 else ''}):")
    lines.append("        step(state)")
    lines.append('    return state["output"]')
    lines.append("")
    return "\n".join(lines)


def _render_test_golden(spec: dict) -> str:
    structural = [c["text"] for c in spec["acceptance_criteria"] if c["check_type"] == "structural"]
    crit = "\n".join(f"    #   - {t}" for t in structural) or "    #   (none tagged structural)"
    return (
        '"""Golden test: the pattern on the original approved input.\n\n'
        "Generated scaffold -- fill the assertions from the spec's structural\n"
        "acceptance_criteria. These same invariants must also hold in\n"
        'test_perturbed.py.\n"""\n'
        "import json\n"
        "from pathlib import Path\n\n"
        "import pipeline\n\n"
        '_INPUTS = Path(__file__).resolve().parent / "inputs"\n\n\n'
        "def test_golden():\n"
        '    data = json.loads((_INPUTS / "golden.json").read_text())\n'
        "    result = pipeline.run(data)\n"
        "    # Structural acceptance criteria to assert:\n"
        f"{crit}\n"
        '    raise AssertionError("TODO(spec-learner): implement golden assertions")\n'
    )


def _render_test_perturbed(spec: dict) -> str:
    structural = [c["text"] for c in spec["acceptance_criteria"] if c["check_type"] == "structural"]
    crit = "\n".join(f"    #   - {t}" for t in structural) or "    #   (none tagged structural)"
    return (
        '"""Perturbed test: the same pattern on a DIFFERENT input.\n\n'
        "Generated scaffold -- create inputs/perturbed.json with different variable\n"
        "values, then assert the structural invariants (NOT equality to the golden\n"
        "output). A failure here means fix the harness or the spec -- never loosen\n"
        'the test.\n"""\n'
        "import json\n"
        "from pathlib import Path\n\n"
        "import pipeline\n\n"
        '_INPUTS = Path(__file__).resolve().parent / "inputs"\n\n\n'
        "def test_perturbed():\n"
        '    data = json.loads((_INPUTS / "perturbed.json").read_text())\n'
        "    result = pipeline.run(data)\n"
        "    # Structural acceptance criteria to assert (must hold here too):\n"
        f"{crit}\n"
        '    raise AssertionError("TODO(spec-learner): implement perturbed assertions")\n'
    )


def _golden_input_from_examples(spec: dict) -> dict:
    """Seed a golden input from the variable fields' example values."""
    out: dict = {}
    for field in _variable_fields(spec):
        if "example" in field:
            out[field["name"]] = field["example"]
    return out


def _render_readme(spec: dict) -> str:
    variable = (
        "\n".join(
            f"- `{f['name']}` ({f['type']}) — {f.get('description', '')}".rstrip()
            for f in _variable_fields(spec)
        )
        or "- (none)"
    )
    fixed = (
        "\n".join(
            f"- `{f['name']}` = `{f.get('example', '')}` — {f.get('description', '')}".rstrip()
            for f in _fixed_fields(spec)
        )
        or "- (none)"
    )
    return (
        f"# Harness: {spec.get('domain', 'task')}/{spec.get('task', 'harness')}\n\n"
        f"{spec['intent']}\n\n"
        "Generated by the spec-learner **hybrid-python** backend. Run it with the\n"
        "uniform CLI:\n\n"
        "```\n"
        "python run.py --input inputs.json --out -\n"
        "```\n\n"
        "## Variable inputs (change per run)\n\n"
        f"{variable}\n\n"
        "## Fixed inputs (constant across runs)\n\n"
        f"{fixed}\n\n"
        "No value marked *variable* above is hardcoded in `pipeline.py`.\n"
    )


def _copy_alongside(spec_path: Path | None, schema_ref: str, dest: Path) -> None:
    if spec_path is None:
        raise ValueError(
            "spec_path is required to resolve the output schema "
            f"({schema_ref!r}); pass it from scaffold.py"
        )
    src = (spec_path.parent / schema_ref).resolve()
    if not src.is_file():
        raise FileNotFoundError(f"output schema not found: {src}")
    dest.write_text(src.read_text())


class HybridPythonBackend:
    name = "hybrid-python"

    def supports(self, spec: dict) -> float:
        if spec.get("target_runtime") != "python":
            return 0.0
        return {"hybrid": 1.0, "deterministic": 0.8}.get(spec.get("harness_type"), 0.0)

    def emit(self, spec: dict, out_dir: Path, spec_path: Path | None = None) -> None:
        out_dir = Path(out_dir)
        out_dir.mkdir(parents=True, exist_ok=True)

        (out_dir / "run.py").write_text(RUN_PY)
        (out_dir / "pipeline.py").write_text(_render_pipeline(spec))
        (out_dir / "README.md").write_text(_render_readme(spec))

        _copy_alongside(
            spec_path, spec["output_contract"]["schema_ref"], out_dir / "output.schema.json"
        )
        if spec_path is not None:
            (out_dir / "pattern-spec.json").write_text(spec_path.read_text())

        if _has_semantic(spec):
            (out_dir / "semantic.py").write_text((_HERE / "semantic.py").read_text())
            prompts = out_dir / "prompts"
            prompts.mkdir(exist_ok=True)
            for sid in spec["semantic_steps"]:
                p = prompts / f"{sid}.txt"
                if not p.exists():
                    p.write_text(f"TODO(spec-learner): prompt for semantic step {sid!r}.\n")

        (out_dir / MANIFEST_NAME).write_text(
            json.dumps(
                {
                    "backend": self.name,
                    "generated_by": "spec-learner",
                    "domain": spec.get("domain"),
                    "task": spec.get("task"),
                },
                indent=2,
            )
            + "\n"
        )

    def emit_tests(self, spec: dict, out_dir: Path, spec_path: Path | None = None) -> None:
        out_dir = Path(out_dir)
        tests = out_dir / "tests"
        inputs = tests / "inputs"
        inputs.mkdir(parents=True, exist_ok=True)

        (tests / "conftest.py").write_text(CONFTEST_PY)
        (tests / "test_golden.py").write_text(_render_test_golden(spec))
        (tests / "test_perturbed.py").write_text(_render_test_perturbed(spec))
        (inputs / "golden.json").write_text(
            json.dumps(_golden_input_from_examples(spec), indent=2) + "\n"
        )

    def verify(self, out_dir: Path) -> VerifyResult:
        out_dir = Path(out_dir)
        report: dict = {"backend": self.name, "runs": [], "qualitative": []}

        # 1) Structural checks: the harness's own pytest suite.
        proc = subprocess.run(
            [sys.executable, "-m", "pytest", str(out_dir / "tests"), "-q"],
            capture_output=True,
            text=True,
        )
        tests_passed = proc.returncode == 0
        report["pytest_returncode"] = proc.returncode
        report["pytest_output"] = (proc.stdout + proc.stderr)[-4000:]

        # 2) Uniform CLI contract: each fixture must produce schema-valid output.
        cli_passed = True
        inputs_dir = out_dir / "tests" / "inputs"
        for eval_id, fixture in enumerate(sorted(inputs_dir.glob("*.json")), start=1):
            start = time.monotonic()
            run = subprocess.run(
                [sys.executable, str(out_dir / "run.py"), "--input", str(fixture), "--out", "-"],
                capture_output=True,
                text=True,
            )
            ok = run.returncode == 0
            cli_passed = cli_passed and ok
            report["runs"].append(
                {
                    "configuration": "with_skill",
                    "eval_id": eval_id,
                    "fixture": fixture.name,
                    "passed": ok,
                    "duration_ms": round((time.monotonic() - start) * 1000),
                    "tokens_used": 0,
                }
            )
            if not ok:
                report["runs"][-1]["stderr"] = run.stderr[-500:]

        # 3) Qualitative criteria need the judge subagent (in-session only).
        spec_file = out_dir / "pattern-spec.json"
        if spec_file.is_file():
            spec = json.loads(spec_file.read_text())
            for crit in spec.get("acceptance_criteria", []):
                if crit["check_type"] == "qualitative":
                    report["qualitative"].append(
                        {"text": crit["text"], "passed": None, "evidence": "skipped (needs judge)"}
                    )

        return VerifyResult(passed=tests_passed and cli_passed, report=report)
